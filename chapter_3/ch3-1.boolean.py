# Boolean
print(True)
print(False)


# 비교연산자
print(10 == 100)
print(10 != 100)
print(10 < 100)
print(10 > 100)
print(10 <= 100)
print(10 >= 100)


# 문자열 비교연산자 : 사전 순으로 크기
print("가방" == "가방")
print("가방" != "하마")
print("가방" < "하마")
print("가방" > "하마")


# 변수가 범위 내부에 있는지 확인하기
x = 25
print(10 < x < 30)
print(40 < x < 60)


# 논리연산자 not
print(not True)
print(not False)

# not 연산자 조합하기기
x= 10
is_under_20 = x < 20
print("is_under_20: ", is_under_20)
print("not is_under_20: ", not is_under_20)

# and 연산자
print(True and True)
print(True and False)
print(False and True)
print(False and False)
print()

# or 연산자
print(True or True)
print(True or False)
print(False or True)
print(False or False)


