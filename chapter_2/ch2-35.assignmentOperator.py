# 숫자 복합 대입	연산자
number = 100
number += 10                # number = number + 10
number += 20
number += 30
print("number:", number)    # 숫자 계산 후 대입

# 문자열	복합	대입	연산자
string = "안녕하세요"
string += "!"
string += "?"
print("string:", string)    # 문자열 연결 후 대입

string = "안녕하세요"
string *= 3
print("string:", string)    # 문자열 반복 후 대입




